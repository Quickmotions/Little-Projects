import itertools
def check_score(matrix):
    newmatrix = []
    matrix = list(itertools.chain.from_iterable(matrix))#flatten list
    for item in matrix:
        newline = int(item.replace(item, str(values[item])))#replace dict values into list as int
        newmatrix.append(newline)#store new list
    return sum(newmatrix)

